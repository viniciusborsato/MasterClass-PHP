	// "ceil" arredonda frações para cima
	echo ceil(4.3) . "<br>";    // 5
	echo ceil(9.999) . "<br>";  // 10
	echo ceil(-3.14) . "<br>";  // -3
	
	echo "<br><br>";
	
	// "floor" arredonda frações para baixo
	echo floor(4.3) . "<br>";   // 4
	echo floor(9.999) . "<br>"; // 9
	echo floor(-3.14) . "<br>"; // -4
